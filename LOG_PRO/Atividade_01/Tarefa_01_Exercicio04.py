#4. Média de 3 notas
#Calcule a média de três valores.
print("Calcularemos qual a média de três valores digitados.") 
numero1 = float(input("Digite o primiero número:"))
numero2 = float(input("Digite o segundo número:"))
numero3 = float(input("Digite o terceiro número:"))
print("A média desses três números é:", (numero1+numero2+numero3) / 3)
notafinal= int((numero1+numero2+numero3) / 3)
print("Arrendondando a nota Final:", notafinal)
