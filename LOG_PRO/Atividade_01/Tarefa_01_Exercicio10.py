#10. Consumo de combustível
#Peça:distância percorrida
#Peça:combustível gasto
#Mostre:consumo médio (km/l)
print("Calcularemos qual o Consumo Médio(km/l) gasto de combustível em uma determinda distância.")
distancia = float(input("Qual a distância peccorrrida em KM ? "))
combustivel = float(input("Qual a quantidade de litros de gastos para pecorrer essa distância ? "))
consumo_medio = combustivel/distancia
print ("O Consumo Médio(km/l) foi de:",consumo_medio,"litros")