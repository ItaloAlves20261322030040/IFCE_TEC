#6. Temperatura
#Converter Celsius para Fahrenheit:
#👉 Fórmula:F = (C * 9/5) + 32
print("Calcularemos qual a correspondência de termperatura de ºCelsius para ºFahrenheit.")
celsius=float(input("qual a temperatura atual em graus Celsius (ºC)? "))
Fahrenheit=(celsius * 9/5) + 32
print("A temperatura correspodente em graus Fahrenheit (ºF) é dê: ",Fahrenheit)

